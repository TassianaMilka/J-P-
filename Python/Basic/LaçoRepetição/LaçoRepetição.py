#Crie um código sobre o laço de repetição que imprima vários números e realiza uma multiplicação

#Imprimir a mensagem
print("Imprimir os números e múltiplicar a ele mesmo\n")

#Laço de repetição
for i in range(4, 6):
     
    #Imprimir o resultado do laço de repetição
    print(i)
    
